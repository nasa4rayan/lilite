function fromTuples(prefix: string, tuples: PackageTuple[]): PackageItem[] {
  return tuples.map(([key, name, category, packageName, icon, logoUrl]) => {
    const description = packageDescriptions[packageName] ?? categoryDescriptionFallback[category]
    const finalLogoUrl = logoUrl ?? packageLogoFallback[packageName]

    return {
      id: `${prefix}-${key}`,
      name,
      category,
      packageName,
      icon,
      description,
      logoUrl: finalLogoUrl,
    }
  })
}

